// + - * / % **

const dois = 2;
console.log(dois + 5); // 7 
console.log(dois - 5); // -3
console.log(dois * 5); // 10
console.log(10 / dois); // 5
console.log(dois ** 10); // potência; 1024
console.log(11 % dois); // mod; 1

let u = 3;
u++; // u += 2; u = u + 2;
console.log(u); // 4
console.log(u++); // pós-incremento 
console.log(u); // 5
console.log(++u); // 6 pré-incremento

